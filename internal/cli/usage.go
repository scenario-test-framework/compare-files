package cli

// Usage は Options 部分の Usage テキストを返します。
// Java 版 CompareFilesOption.usage() の文言を踏襲しています
// (旧版の誤記 -ddo は実装に合わせて -wdo に修正済み)。
func Usage() string {
	return `  Options:
    -h, --help
      このメッセージを表示します。

    -v, --version
      バージョンを表示します。

    -config, --configFile
      設定ファイルのパスを指定します。

    -lint, --lintLayout
      比較レイアウト定義ファイルを検証して終了します。
      エラーなし: 0 / エラーあり: 6 を返します。


    ----- 以降の設定を指定すると、設定ファイルより優先されます。 -----

    -ignore, --ignoreItemList
      比較除外項目名をカンマ区切りで指定します。
      レイアウト定義の設定を上書きして、指定された項目の比較を除外します。

    -layout, --overwriteLayoutDir
      ${comparefiles_root}/config/compare_layout 配下のレイアウト定義に加えて、指定したディレクトリ配下のレイアウト定義を読込みます。
      同一の物理ファイル名正規表現を持つレイアウトが存在する場合、後勝ち で上書きします。
      ※例）config配下に「定義書から自動生成したレイアウト」を配置。
         テストで「共通適用するレイアウト」、「今回のケースだけに適用するレイアウト」
         を別ディレクトリで管理。実行時に上記の記載順に指定する。 など

    -d, --deleteWorkDir
      作業ディレクトリを削除する場合に指定します。

    -s, --sorted
      比較対象ファイルが、比較キー項目でソート済の場合に指定します。
      内部的にソート後に、比較処理を行っているため、事前にソートされていると高速化が期待できます。

    -ch, --csvHeaderRow
      CSV / TSV ファイルの比較時に必須です。
      ヘッダー行番号を指定します。

    -cd, --csvDataStartRow
      CSV / TSV ファイルの比較時に必須です。
      データ開始行番号を指定します。

    -od, --outputDir
      出力ディレクトリを指定します。

    -oc, --outputCharset
      出力ファイルの文字コードを指定します。

    -of, --compareResultFileName
      比較結果ファイル名を指定します。

    -dfp, --compareDetailFilePrefix
      比較詳細ファイル名のプリフィックスを指定します。
      ファイル名の形式は ${プリフィックス}${比較対象ファイル名}.csv です。

    -wdo, --writeDiffOnly
      比較詳細ファイルの出力内容を 差分のみ にする場合に指定します。

    -dpl, --leftPrefix
      比較詳細ファイルの差分出力で 左ファイルの項目を表示するときに利用するプリフィックスを指定します。

    -dpr, --rightPrefix
      比較詳細ファイルの差分出力で 右ファイルの項目を表示するときに利用するプリフィックスを指定します。

    -chunk, --chunkSize
      出力バッファサイズを行数で指定します。
      比較結果の出力時に、逐次で出力するとFileI/Oで処理時間がかさむためバッファリングしています。
      リソースや比較対象のデータサイズに合わせてサイズを調整することで高速化が期待できます。

`
}
